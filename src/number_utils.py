def number_is_even(n: int) -> bool:
    """
    Verify the number is an even number
    """
    return n % 2 == 0


def sum_of_numbers(n: int, m: int) -> int:
    """
    Sums the numbers
    """
    return n * m


def number_divisible_by_5(n: int) -> bool:
    """
    Verify that the number is divisible by 5
    """
    return n % 5 == 0
